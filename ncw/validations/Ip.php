class Ncw_Validations_IP extends Ncw_Validation
{

    /**
     * The error message
     *
     * @var string
     */
    public $error_message = 'This value is not a valid IP!';

    /**
     * Checks if the given value is of
     * IP type.
     *
     * @param mixed $value the value to check
     *
     * @return boolean true or false
     */
    public function check ($value)
    {
        // If the function filter_var is accesable
        if (true === function_exists("filter_var")) {
            // Check if is a valid IPv4 or IPv6 address.
            // Reserved (exp:255.255.255.255) networks are not valid
            if (false !== filter_var($value, FILTER_VALIDATE_IP, array("FLAGS" => FILTER_FLAG_NO_RES_RANGE))) {
                return true;
            }
        } else {
            // Check if valid IPv4 address with the function ip2long.
            if ($value == long2ip(ip2long($value))) {
                return true;
            }
            // Check if valid IPv6 address
            $ipv6_pattern = "/^((([0-9A-Fa-f]{1,4}:){7}[0-9A-Fa-f]{1,4})|(([0-9A-Fa-f]{1,4}:){6}:[0-9A-Fa-f]{1,4})" . "|(([0-9A-Fa-f]{1,4}:){5}:([0-9A-Fa-f]{1,4}:)?[0-9A-Fa-f]{1,4})|(([0-9A-Fa-f]{1,4}:){4}" . ":([0-9A-Fa-f]{1,4}:){0,2}[0-9A-Fa-f]{1,4})|(([0-9A-Fa-f]{1,4}:){3}:([0-9A-Fa-f]{1,4}:)" . "{0,3}[0-9A-Fa-f]{1,4})|(([0-9A-Fa-f]{1,4}:){2}:([0-9A-Fa-f]{1,4}:){0,4}[0-9A-Fa-f]{1,4})" . "|(([0-9A-Fa-f]{1,4}:){6}((\b((25[0-5])|(1\d{2})|(2[0-4]\d)|(\d{1,2}))\b)\.){3}(\b((25[0-5])" . "|(1\d{2})|(2[0-4]\d)|(\d{1,2}))\b))|(([0-9A-Fa-f]{1,4}:){0,5}:((\b((25[0-5])|(1\d{2})|(2[0-4]\d)" . "|(\d{1,2}))\b)\.){3}(\b((25[0-5])|(1\d{2})|(2[0-4]\d)|(\d{1,2}))\b))|(::([0-9A-Fa-f]{1,4}:)" . "{0,5}((\b((25[0-5])|(1\d{2})|(2[0-4]\d)|(\d{1,2}))\b)\.){3}(\b((25[0-5])|(1\d{2})|(2[0-4]\d)" . "|(\d{1,2}))\b))|([0-9A-Fa-f]{1,4}::([0-9A-Fa-f]{1,4}:){0,5}[0-9A-Fa-f]{1,4})|(::([0-9A-Fa-f]" . "{1,4}:){0,6}[0-9A-Fa-f]{1,4})|(([0-9A-Fa-f]{1,4}:){1,7}:))$/";
            if (true == preg_match($ipv6_pattern, $value)) {
                return true;
            }
        }
        return false;
    }
}
